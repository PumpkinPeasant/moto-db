def strip_district_name(raw: str) -> str:
    raw = raw.strip()
    lower = raw.lower()
    if lower.startswith("район "):
        return raw[len("район "):].strip()
    if lower.endswith(" район"):
        return raw[: -len(" район")].strip()
    return raw
